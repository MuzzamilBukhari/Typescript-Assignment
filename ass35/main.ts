let animals: string[] = ["dog", "cat", "rabbit"];

animals.forEach(animal => {
  if (animal == "dog"){
    console.log("Dogs are loyal companions known for their affectionate nature and diverse breeds.");
  } else if (animal == "cat"){
    console.log("Cats are independent yet affectionate pets, valued for their agility and calming presence.");
  } else if (animal == "rabbit"){
    console.log("Rabbits are gentle and social animals, popular for their soft fur and curious personalities.");
  }
})

// Print what these animals have in common
console.log("All three animals are commonly kept as beloved pets for their companionship and distinct personalities.");
